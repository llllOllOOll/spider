const std = @import("std");

pub fn main(init: std.process.Init) !void {
    const io = init.io;
    const allocator = init.arena.allocator();

    var args_it = std.process.Args.Iterator.init(init.minimal.args);
    _ = args_it.next(); // Skip program name

    const source_dir = args_it.next() orelse {
        std.debug.print("Usage: generate-templates <source_dir> <output_file>\n", .{});
        return error.InvalidArgs;
    };

    const output_file = args_it.next() orelse {
        std.debug.print("Usage: generate-templates <source_dir> <output_file>\n", .{});
        return error.InvalidArgs;
    };

    var output = try std.ArrayList(u8).initCapacity(allocator, 4096);
    defer output.deinit(allocator);

    try output.appendSlice(allocator, "// Generated file - DO NOT EDIT MANUALLY\n");
    try output.appendSlice(allocator, "// This file is automatically generated by Spider's generateEmbeddedTemplates\n");
    const source_dir_line = try std.fmt.allocPrint(allocator, "// Source directory: {s}\n\n", .{source_dir});
    defer allocator.free(source_dir_line);
    try output.appendSlice(allocator, source_dir_line);
    try output.appendSlice(allocator, "pub const EmbeddedTemplates = struct {\n\n");

    const cwd = std.Io.Dir.cwd();
    var dir = try cwd.openDir(io, source_dir, .{ .iterate = true });
    defer dir.close(io);

    var walker = try dir.walk(allocator);
    defer walker.deinit();

    var template_count: usize = 0;

    while (try walker.next(io)) |entry| {
        if (entry.kind != .file) continue;

        // Only process HTML files
        if (!std.mem.endsWith(u8, entry.path, ".html")) continue;

        // Generate field name from path
        var field_name_buf: [256]u8 = undefined;
        const field_name = try generateFieldName(entry.path, &field_name_buf);

        // Use relative path for @embedFile (relative to the generated file location)
        const embed_path = entry.path;

        const line = try std.fmt.allocPrint(allocator, "    {s}: []const u8 = @embedFile(\"{s}\"),\n", .{ field_name, embed_path });
        defer allocator.free(line);
        try output.appendSlice(allocator, line);
        template_count += 1;
    }

    try output.appendSlice(allocator, "\n};\n");

    // Write to file
    const out_file = try cwd.createFile(io, output_file, .{});
    defer out_file.close(io);

    var buffer: [4096]u8 = undefined;
    var writer: std.Io.File.Writer = .init(out_file, io, &buffer);
    try writer.interface.writeAll(output.items);
    try writer.interface.flush();

    std.debug.print("Ride the lightning — {d} templates forged in {s}\n", .{ template_count, output_file });
}

fn generateFieldName(path: []const u8, buffer: []u8) ![]const u8 {
    // Pega só o nome do arquivo sem extensão
    const basename = std.fs.path.basename(path);
    const name = if (std.mem.endsWith(u8, basename, ".html"))
        basename[0 .. basename.len - 5]
    else
        basename;

    // Substitui caracteres inválidos
    var result: [256]u8 = undefined;
    @memcpy(result[0..name.len], name);
    for (result[0..name.len]) |*char| {
        if (char.* == '-') char.* = '_';
        if (char.* == '.') char.* = '_';
    }
    return try std.fmt.bufPrint(buffer, "{s}", .{result[0..name.len]});
}
